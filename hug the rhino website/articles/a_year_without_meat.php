        <div class="col-sm-8">
          <p><span class="dropcap">F</span>or the last year I've tried out a vegetarian diet. I never thought I could give up meat, mostly because it tasted so good. But I started reducing my meat intake after learning about the impacts of factory farming (read more about that journey here), and eventually figured I'd try out being vegetarian.</p>
          <p>It was pretty fantastic. And very visible. Before I had been a flexitarian, meaning I'd eat meat whenever I had no other choice. Now I was forced to alert friends of my diet, refuse food at times, and publicly declare my convictions. It went a lot better than I thought it might. If you are thinking about giving up meat, lessening your meat intake, or if you just want to read more about the experience, here are the top 7 lessons I learned from being vegetarian for a year.</p>
          <h1>1. It was a lot easier than I expected</h1>
          <p>Doing my own cooking and shopping made it easy to avoid meat, and around 99% of restaurants I visited had a vegetarian option (so many veggie burgers). Initially I was worried about being a burden on friends or hosts, but people were more than happy to accommodate my diet if I gave them advance notice. My friends and family all had different opinions, mostly thinking I was crazy, but overall they supported my choices.</p>
          <p>Part of that ease came from experience and preparation. I’d spent six months eating meat once or twice a month, then six months before eating meat once a week. With this gradual decline in my meat eating I didn’t really crave meat and knew how to make vegetarian meals and supplement my protein. If you want to try being vegetarian, I'd start by gradually removing meat from your diet over a period of 1-6 months.</p>
          <img src="../images/articles/a_year_without_meat/photo1.jpg" />
          <h1>2. I saved a lot of money</h1>
          <p>Total savings: $750. I saved about $1 every lunch and dinner from skipping the meat, and benefited from cheaper prices for vegetarian dishes at restaurants. Lentils=Money in the bank.</p>
          <h1>3. Vegetarian does not automatically equal healthy</h1>
          <p>You can still eat lots of cookies. Just the act of removing meat doesn't mean replacing it with plant protein, fruits, and vegetables. Knowing this, I used being vegetarian as part of an intentional overhaul in what I eat and consider “desirable” food.</p>
          <p>Luckily, the process was helped by how great I felt. After about a month of effort I settled into a groove of healthy eating that was self-reinforcing. It worked something like this - I ate lots of vegetables and fruits, which made my body feel great, which made me want to eat more good food, which made me feel even better, and so on.  I still indulge in the finer things in life - dark chocolate, craft beer, coconut milk ice cream, but now I enjoy the food I "should be eating" just as much if not more than the food that I shouldn't. To really eat healthy, you have to start loving the experience of wholesome food, which takes time.</p>
          <img src="../images/articles/a_year_without_meat/photo2.jpg" />
          <h1>4. Be ready for lots of questions</h1>
          <p>Most people and quite curious about someone not choosing to eat meat. Here are some of the questions I get all of the time, along with my responses.</p>
          <h3>Why change your diet?</h3>
          <p>Again, here is the link to last year’s blog on <a href="http://www.hugtherhino.com/2013/02/why-we-should-all-become-vegetariansish.html">going vegetarian</a> for a more in depth explanation, but the elevator pitch is simple.</p>
          <ul>
            <li><strong>Health</strong> – Eating more fruits and vegetables makes me feel physically and mentally better.</li>
            <li><strong>Sustainability</strong> – Factory Farming of animals does an unbelievable amount of damage to the environment, with more greenhouse emission than the entire transportation sector (planes, cars, trains, etc.) See <a href="http://media.mnn.com/sites/default/files/user-35/vegan%20infographic.jpg">this infographic</a> for more details</li>
            <li><strong>Ethics</strong> – I can eat and be healthy without killing animals or causing them suffering.</li>
          </ul>
          <h3>How do you get protein?</h3>
          <p>My meals are a combination of vegetables (fresh tomatoes, broccoli, carrots, etc. etc. etc.) , protein packed beans, lentils and quinoa, spinach, lots of nuts, and other scrumptious ingredients. Veggies and hummus, carrots and peanut butter, and trail mix are go to/can’t miss/live for snacks.</p>
          <h3>Humans have been eating meat forever. Why stop now?</h3>
          <p>Just because something has been happening for a long time does not mean it should persist into perpetuity. We have to evaluate the positives and negatives of any tradition to assess if we want it to continue.</p>
          <h3>Do you feel weak?</h3>
          <p>While being vegetarian I trained six days a week in swimming, biking, and running, raced two triathlons, ran a half-marathon, and felt physically and mentally sharper than ever. So I feel great!  In fact there have been a slew of <a href="http://www.theguardian.com/lifeandstyle/wordofmouth/2012/jul/30/lizzie-armitstead-vegetarian-athletes-olympics-2012">vegetarian Olympians and great athletes</a>.</p>
          <h3>People will always love meat, so why care?</h3>
          <p>I love meat too. But as I've discovered, meat eating habits can change. Americans eat 150 times more chicken then we did 80 years ago (Source: <a href="http://www.eatinganimals.com/">Eating Animals</a>). 150 times! We could go in the opposite direction. Everyone could easily become a weekday vegetarian (only meat on the weekends, <a href="http://www.ted.com/talks/graham_hill_weekday_vegetarian">described in this video</a>), or eat meat for one meal a day.</p>
          <h1>5. It's off limits to ask questions in return</h1>
          <p>Such as why do you eat meat? While it's not actually illegal, this question comes with some social stigma. And that's frustrating because a double standard is applied to conversations about meat. I get asked every question in the book, but asking back is stepping over the line and becoming an overly-righteous vegetarian. Mostly I just let people ask, and hope that my answers stir an interest in the topic.</p>
          <img src="../images/articles/a_year_without_meat/photo3.jpg" />
          <h1>6. The ethics of killing animals is very complicated</h1>
          <p>These are the kind of questions I’ve been struggling with other the past year.</p>
          <ul>
            <li>If the death of an animal is unnecessary for my survival and health, is it morally ok to kill it?</li>
            <li>Is death different than a life of suffering and imprisonment to harvest the byproducts of a cow or chicken (eggs or milk)?</li>
            <li>Dying is a natural part of life, animals die in nature and all animals will die eventually. Does this give us the right to raise animals specifically to die?</li>
          </ul>
          <p>Every person will have a different answer to the above questions. You might be unhappy with the inhumane treatment of animals and environmental impact of factory farming, and decide to pay more and buy your meat from local farmers. You might decide you don't want to cause any animal deaths, but needs eggs and dairy products in your diet. Regardless of the ending point, everyone should begin the journey and start thinking deeply about the food that you consume.</p>
          <h1>7. Being vegetarian is only one piece of the solution</h1>
          <p>The simple act of choosing to care more about the food that I consume has led an interest in broader food topics. Childhood obesity, urban gardens, food deserts, farmers markets, fish farms, cooking, GMOs, processed food, etc. Here are a few of my favorite TED videos that discuss these wider topics.</p>
          <p><a href="http://www.ted.com/talks/jamie_oliver">Jamie Oliver</a>: Teaching every child about food.</p>
          <p><a href="http://www.ted.com/talks/roger_doiron_my_subversive_garden_plot">Roger Doiron</a> - On how Americans need to start growing our own gardens again.</p>
          <p><a href="http://www.ted.com/talks/dan_barber_how_i_fell_in_love_with_a_fish">Dan Barber</a> - How he fell in love with a sustainable fish farm.</p>
          <p>Along with eating less meat I'm interested in how we can help a generation of Americans learn to cook, care about food, avoid diet-related health problems and pass on that knowledge to their children.</p>
          <h1>From here on out</h1>
          <p>Change is a strange process. It’s a combination of articles, books, movies, and influential relationships that have changed my dietary habits over the course of three years. It takes time, and without some of those pieces I wouldn’t be writing about being a vegetarian.</p>
          <p>My journey to learn more about what I eat, where it comes from, and how it is produced has transformed my life for the better. I'm a more thoughtful, caring, and appreciate person now that I've taken the time to deeply explore my eating habits, and it's an adventure that's just beginning.</p>
          <p>I can't wait for my next meal.</p>
          <p>---</p>
          <p>Kurt Berning</p>
