          <p>Books on Food</p>
          <ul>
            <li>Michael Pollan's "The Omnivore's Dilemma"</li>
            <li>Michael Pollan's "Food Rules"</li>
            <li>Michael Pollan's "In Defense of Food"</li>
          </ul>
          <p>These books are all great intro's to start thinking more about the food that you eat, the time it takes to prepare a meal, and the path of food from farm to plate.</p>
          <ul>
            <li>Peter Singer's "Animal Liberation"</li>
            <li>Jonathan Safran Foer's "Eating Animals"</li>
          </ul>
          <p>These books focus on the ethics of eating animals, with Singer's deep questions about the ethics of eating animals, and Foer's exploration of the suffering present in modern factory farming.</p>
          <p>Movies/Documentaries</p>
          <ul>
            <li>Forks Over Knives (The Priority)</li>
            <li>Fat, Sick, and Nearly Dead</li>
            <li>TED Videos (all the ones listed in the article)</li>
          </ul>
          <p>Forks Over Knives is about eating yourself to health, instead of being cut open on the operating table. Fat, Sick, and Nearly Dead is similar, without being as well done. The TED videos are all great intros to caring about food issues.</p>

