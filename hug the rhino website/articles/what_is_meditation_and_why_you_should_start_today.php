          <p><span class="dropcap">O</span>dds are that your mind is out of shape. When is the last time you sat still for twenty minutes – in silence? When is the last time you were truly present, with nothing on your mind but the person you are with? Can you stop your thoughts and anxieties and appreciate the beauty of a tree, the sky, almond butter? A better life starts with a better mind. In this article I’ll explain a technique of mind training and development called Meditation – Maybe the single greatest technique to improve your quality of life.</p>
          <h1>What is Meditation?</h1>
          <p>Meditation is a technique for transformation of the mind. The vehicle for this transformation is daily periods of intentional time that focus and awaken mental attention. Daily meditation practice trains your mind to go beyond habitual thinking and develop concentration, presence, clarity, awareness, and peace. Through meditation you can explore the nature of reality while being truly aware of what is happening in the present moment.</p>
          <img src="../images/articles/what_is_meditation_and_why_you_should_start_today/photo1.jpg" />
          <h1>Why practice meditation?</h1>
          <p>Meditation physically transforms your brain. Researchers at Harvard and Stanford found physical changes in the brain after an 8-week meditation course. Neurons had moved from areas of the brain that stimulate fear to <a href="https://www.headspace.com/science/meditation-for-stress" target="_blank">regions that fosteremotional control and wellness</a>. Meditation also has great calming effects, <a href="http://alifeofproductivity.com/meditation-guide/" target="_blank">helps your body need less sleep, andreleases your brain from over-thinking</a>. As if those weren’t enough, it can also <a href="https://www.headspace.com/science" target="_blank">decrease anxiety and stress, improveyour relationships, improve your creativity and help you focus</a>.</p>
          <p>Training and exercise has proven benefits for the physical body and is essential for health, happiness and longevity. It’s pretty easy to see the results when someone loses weight or gets in great shape. Training of the mind is practiced much less – possibly because there are no obvious physical changes. Nevertheless, this mental exercise and training has amazing transformational potential.</p>
          <h1>How to Practice Meditation</h1>
          <p>You’ll need:</p>
          <ul>
            <li>A chair/stool</li>
            <li>A timer</li>
            <li>Time</li>
            <li>A few tips</li>
          </ul>
          <h3>A Chair/Stool</h3>
          <p>One of my favorite guided meditations offers this seating advice:</p>
          <p><em>“Start by finding a comfortable position on a firm, straight backed chair or a meditation stool. If you are sitting in a chair, allow your feet to be flat on the floor with your legs uncrossed and your spine straight, so that your posture supports your intention to be awake and aware.”</em> 
          <br>-<a href="seven_guided_meditations_to_jumpstart_your_meditation.php" target="_blank">Meditation of the Body and Breath</a></p>
          <p>I follow those instructions during my meditations. The two keys are comfort and alertness. It might take some time to find a balance of the two.</p>
          <p>The traditional pose also works – Simply sit on the floor or a cushion with crossed legs and a similar alert. This is probably what you think of when you hear “meditation”. I’ve found it’s a little harder on the lower back, but also that your ability to sit comfortably improves over time.</p>
          <p>Location Tip - Try to find a quite space to listen to the meditations, or use headphones in a louder space.</p>
          <h3>Timer</h3>
          <p>A timer is essential for recording your meditations and keeping time during non-guided sessions.  I prefer App’s to do the work for me here, and I use Insight Timer – It’s free, has built-in guided meditations, and keeps tracks of all of my meditation data. <a href="http://buddhify.com/" target="_blank">Buddhify</a> and <a href="https://itunes.apple.com/us/app/meditate-meditation-timer/id322538701?mt=8" target="_blank">Meditate</a> are also great options, available for $2.99 and $3.99 respectively. I’ve found these Apps to be far superior to a pen and paper journal where you have to keep track of my own data.</p>
          <p>To record your guided meditations, simply use the “timer” in the App and set it to the length of the guided meditation.</p>
          <h3>Time</h3>
          <p>A great starting goal is 10 minutes a day of meditation. Pick a time each day by finding a number where there is no resistance.</p>
          <p>As wonderfully stated in <a href="http://alifeofproductivity.com/simple-secret-to-meditating-every-day/" target="_blank">A Year of Productivity…</a></p>
          <p><em>“I just shrink the length of the session in my head until I hit a level I don’t feel resistance to. Like, “Could I do 15 minutes? No, I feel resistance, I’m not gonna do it. Okay, what about 10? Still too long, the thought puts me off. Okay, 5? Huh, I don’t feel resistance to that. I feel like I can sit for 5. Boom.”</em></p>
          <p>Since I began meditating about a year ago, there are days where I haven’t meditated or only been able to manage a single minute of silence. That’s ok, it’s part of the process. It’s also possible to practice meditation without taking time to sit still. When <a href="http://alifeofproductivity.com/top-10-things-learned-productivity-meditating-for-35-hours/" target="_blank">Chris Bailey meditated for 35 hours</a> in one week, he spent more than half of his meditation time doing chores, walking, and eating.</p>
          <img src="../images/articles/what_is_meditation_and_why_you_should_start_today/photo2.jpg" />
          <h3>A Few Tips</h3>
          <ol>
            <li>Don’t try too hard- Slowly train your mind and improve. Bring your mind to a natural place of rest.</li>
            <li>Don’t try to create happiness and calm – instead create a space in your mind for them to occupy.</li>
          </ol>
          <p>These are the lessons from a series of one minute <a href="http://www.theguardian.com/lifeandstyle/video/2012/jan/06/health-and-wellbeing-fitness" target="_blank">videos</a> that explain more about how and why to meditate. I would recommend watching them before beginning your practice.</p>
          <p>Bringing this back again to the physical exercise metaphor, if you haven’t been physically active the best way to start isn’t to run 10 miles. Start slow, meditate with a group or a friend, be consistent, work through challenges and obstacles, and slowly improve the quality and length of your training.</p>
          <h1>Other Tools to Begin Your Practice</h1>
          <p><a href="seven_guided_meditations_to_jumpstart_your_meditation.php" target="_blank">7 Guided Meditations</a> to Jump Start Your Meditation Practice – See our pick of the best Guided meditations to help you continue your practice.</p>
          <p>Tara Brach’s “<a href="http://tarabrach.com/how-to-meditate.pdf" target="_blank">How to Meditate</a>” - I’ve tried to make these instructions as succinct as possible. For a longer explanation, read Tara’s guide. It includes some especially useful information on “Sustaining a Practice” and “Common Issues for Meditators”.</p>
          <p>A Year of Productivity’s <a href="http://alifeofproductivity.com/meditation-guide/" target="_blank">Guide to Start Meditation</a>. Provides a good description of breathing meditation, and has a broader wealth of information on meditation.</p>
          <p>Good luck starting your meditation practice. In the words of one of my favorite <a href="#" target="_blank">guided meditations:</a></p>
          <p><em>“May you be well, may you be happy, and may you have ease of being.”</em></p>
