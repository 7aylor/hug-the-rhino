          <p><span class="dropcap">I</span>f you'd asked me four years ago I would have laughed at the idea of becoming vegetarian, saying something like:</p>
          <p>"I love meat, how could I give that up?"</p>
          <p>Three years ago, I'd probably laugh again, draw upon college experience and say:</p>
          <p>"Human teeth were shaped by evolution to eat both meat and plants. Being a vegetarian is against human nature."</p>
          <p>Yet for the last two years I've consciously made the decision to eat less and less meat. At first it was meat two or three times a week, then it dropped to once a week, once every two weeks, and then to about once a month. How could this happen?</p>
          <p>Turns out I have a girl to blame. And science. The usual suspects.</p>
          <img src="../images/articles/we_should_eat_less_meat/photo1.jpg" />
          <h1>Flexitarians</h1>
          <p>Although it sounds a bit like a yoga expert, a flexitarian is someone who chooses not to eat meat when possible, but will eat meat on special occasions (when it's already prepared by friends, or a goat is slaughtered in your honor, etc). The frequency of meat eating is really up to the person, but it's normally pretty infrequent. When I started dating a flexitarian, I was intrigued by this idea, but didn't really have any good reasons for reducing my meat consumption. I love meat! Why would I eat less of something I love?</p>
          <p>Then I read Michael Pollan's "<a href="http://www.amazon.co.uk/Omnivores-Dilemma-Search-Perfect-Fast-Food/dp/1408812185">Omnivores Dilemma</a>" and started thinking about not only what I ate but the conditions in which it was produced.</p>
          <p>Learning that eating less meat would have a positive impact on my health, and less meat production would be better for the environment (data here), I made the decision to start reducing my meat consumption.</p>
          <h1>Life without Meat*</h1>
          <p>*Almost without meat.</p>
          <p>For two years, things have been wonderful. I've been able to eat meat when needed and abstain when possible. I thought I'd miss meat a lot more than I actually do. I was also worried about supplementing the protein in my diet, which has turned out to be easy (beans, lentils, soy, quinoa, spinach, eggs, lots of nuts).</p>
          <p>Currently I'm working out around 1 hour per day, six days a week,  and eating meat once a month. I'm in the best all-around shape of my life and have never eaten healthier. Up until two weeks ago, I figured this would go on for the rest of my life. Meat when necessary, but 99% without.</p>
          <img src="../images/articles/we_should_eat_less_meat/photo2.jpg" />
          <h3>The Ethical Component</h3>
          <p>As explained,  my decision was entirely personally and environmentally oriented. Better for me, and better for the planet. Easy. And the animals killed to make meat? Pssh, they aren't important. As I'd told people,</p>
          <p>"I'm not one of those people doing it for the animals"</p>
          <p>To tell you the truth, I had never really thought about the animals involved in the meat industry. I only knew that factory farms were awful for the environment, and yes, animals were held in terrible conditions. But when I realized the truth, it rocked the foundations of my former certainty.</p>
          <p><a href="https://www.youtube.com/watch?v=ibuQ-J04eLQ">Please watch this documentary, "Earthlings", which offers a uncompromising view of human relations with animals</a>.  It's tough to get through, but worth it. I guess like most things. It's split into five parts, Pets, Food, Clothes, Entertainment, Scientific Research, and talks about treatment of animals in each industry. I had to take an emotional break halfway though. It's powerful, and startling.</p>
          <p>WARNING: This video is extremely graphic in it's depiction of the state of animals in various industries.</p>
          <img src="../images/articles/we_should_eat_less_meat/photo3.jpg" />
          <h1>So What Now?</h1>
          <p>Well I'm torn. And shocked. And saddened. Now that I've seen the suffering of such animals, it only adds to the evidence about the health and environmental impact of meat.  Should I become a full vegetarian, forever? Should I eat meat once or twice a year? Can I eat meat again? (I haven't since watching the documentary, and won't for a while). I've still got many things to puzzle out. But one thing is clear.</p>
          <p>"And if you've come this far, maybe you're willing to come a little further."</p>
          <p>-Shawshank Redemption</p>
          <p>It's time for our society to make a change. It's time that we all take action to eat significantly less meat.</p>
          <h1>Progress Update - 2015</h1>
          <p>I decided to try out being Vegetarian for all of 2013. Read about that experience here "<a href="a_year_without_meat.php">A Year Without Meat</a>" Given that the same problems with meat are inherent to cheese, eggs, and dairy, I decided to try out a Vegan diet for 2014. I'm planning to stay Vegan for the foreseeable future.</p>
          <p>New inspiration from Jonathan Safran Foer with his book <a href="http://www.eatinganimals.com/">Eating Animals</a>, and his <a href="http://www.nytimes.com/2009/10/11/magazine/11foer-t.html?pagewanted=all&_r=1&">NY Times article</a>.</p>


