          <p><span class="dropcap">L</span>istening to guided meditations is one of the best ways to reinvigorate your practice or start daily meditation. With guided meditations, you can let someone else be the expert. Listen to their voice as their guidance and insight lead you to greater understanding of different aspects of presence and mindfulness.</p>
          <p>Before starting the guided meditations, it might be helpful to understand basics of meditation. See our <a href="what_is_meditation_and_why_you_should_start_today.php" target="_blank">Meditation Guide for more information.</a></p>
          <p>Armed with that basic guide, here are some of the best Guided Meditations that I’ve heard. Start with the simple, shorter and more basic meditations and move your way toward longer meditations.</p>
          <h1>Basic Guided Meditations</h1>
          <h3>1. <a href="http://cdn.franticworld.com/wp-content/uploads/2012/02/Mindfulness-Of-Body-And-Breath-from-book-Mindfulness-Finding-Peace-in-a-Frantic-World-128k.mp3" target="_blank">Meditation of the Body and Breath</a> (8 min)</h3>
          <p>This was the first guided meditation that I ever completed, and it’s a fantastic place to start. It’s simple and straightforward, and the speaker is often silent in order to let you practice being present and alert.</p>
          <h3>2. <a href="http://cdn.franticworld.com/wp-content/uploads/2012/02/Sounds-and-thoughts-meditation-from-book-Mindfulness-Finding-Peace-in-a-Frantic-World-128k.mp3" target="_blank">Sounds and Thoughts</a> (8 min)</h3>
          <p>A great meditation for gaining a new perspective on your environment, be it your apartment, home or a nearby park. Learn a new appreciation for sounds and see how listening can teach us to let our thoughts pass and achieve a calmer mind.</p>
          <h3>3. <a href="http://cdn.franticworld.com/wp-content/uploads/2012/02/Three-Minute-Breathing-Space-meditation-from-book-Mindfulness-Finding-Peace-in-a-Frantic-World-128k.mp3" target="_blank">A Quick Breathing Space</a> (3 min)</h3>
          <p>Don’t have time for a long meditation? Just take a three minute breathing space, and become present. Or even <a href="http://ayearofproductivity.com/one-minute-gift/" target="_blank">1 minute!</a></p>
          <h1>Advanced Guided Meditations</h1>
          <h3>4. <a href="https://www.youtube.com/watch?v=1sQ0DTF2tAM" target="_blank">Nature of Awareness</a> – Eckhart Tolle (13 min)</h3>
          <p>A fascinating meditation, one of my favorite voices as well. Eckhart explains how to find presence and clarity and how to “Step out of Thought.”</p>
          <h3>5. <a href="https://www.youtube.com/watch?v=HAoNlfmiPGw" target="_blank">Stepping Inwards</a>  (15 min)</h3>
          <p>The sounds and journey of this meditation are superb. It is a very peaceful and fulfilling meditation that encourages you to explore the core of your being.</p>
          <h3>6. <a href="http://www.tarabrach.com/video/2011-10-05-Divine-Abodes--Lovingkindness.html" target="_blank"> Loving Kindness</a> – Tara Branch. Starts at 41:15 (18 min.)</h3>
          <p>A classic loving kindness meditation, this helps you extend love and peace to yourself, close family, stranger and even enemies. I especially enjoy the physical placing of the hand over the heart while wishing yourself wellness during this meditation.</p>
          <h3>7. <a href="http://www.meditainment.com/the-secret-garden/" target="_blank">The Secret Garden</a> – Meditainment (20 min)</h3>
          <p>A unique meditation experience – The secret garden combines meditation and imagination to create a beautiful sanctuary for the mind. In this space you have a chance to gain a new perspective on a troubling issue in your life or simply deepen your meditation practice.</p>
          <p>I hope these meditations bring you acceptance, presence, and peace.</p>
