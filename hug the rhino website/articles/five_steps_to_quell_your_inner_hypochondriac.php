          <p><span class="dropcap">I</span> know how embarrassing it is to look at yourself in the mirror and realize you’re overreacting without any semblance of rational thought. Again. All because you Googled "weird skin rash," and now you're convinced you have flesh-eating bacteria and twenty-four hours to live.</p>
          <p>I’ve always been a worry-wart, a stressor, an anxiety tornado. When my little sister was born, I scurried up to my mom's hospital bed and stared at her, eyes wide, my brain churning outrageous ideas about how that mini-person came out of her body. “I’ve never seen your eyes so big,” she recounts to me, “you were so scared.” That tendency has unfortunately stuck with me for my entire life.</p>
          <p>I used to read Women’s Health religiously. One day there happened to be an article about a woman who lived a completely healthy life; she didn’t smoke, she ate fruits and veggies, and she exercised regularly. She was your model health nut until she discovered she had throat cancer. Wait, what? I did a little research on the Internet to find out more about throat cancer, and after convincing myself that I probably had it too, I caused myself ten times more stress and spent money on doctor’s visits that I didn’t need.</p>
          <p>I needed to find a way to digest information like this article without buying into scare tactics. If you ever find yourself fretting over something without any reason or cause, follow these steps:</p>
          <h1>1. Ask - what is the purpose of this article?</h1>
          <p>Is it to inform, shock, entertain? Take a look at the context that surrounds the article and decide what the piece wants you to think or do. You wouldn’t trust health advice from Us or People Magazine, would you?</p>
          <h1>2. Disconnect yourself from the person in the story</h1>
          <p>Writers will describe the person as an average, everyday man or woman to make him or her as relatable as possible. “*Gasp!* She eats vegetables, exercises, and has brown hair? So do I! She lived in Denver? So did I!” Realize that each person is different from the next, and that making the subject of the story as relatable as possible is what draws you to keep reading. Don’t put yourself in their shoes.</p>
          <h1>3. Do objective research after you’re finished reading</h1>
          <p>Despite what you just gulped down in a hypochondriatic frenzy, you need to conduct your own research. Find the most credible and objective sources possible, such as the Center for Disease Control or reputable medical journals. Ignore the Yahoo! Answers and other discussion boards. Look at the bottom of any article you read for the sources it uses; does it have any?</p>
          <h1>4. Breathe</h1>
          <p>Find something to calm yourself. When we’re aroused, excited or angry, we tend to think illogically and jump to the most dramatic conclusion. Take a walk, listen to calming music, or do some slow, deep breathing to get back to your normal state of mind.</p>
          <h1>5. Schedule a doctor’s appointment</h1>
          <p>If none of these steps helps, and you have a legitimate reason for concern, schedule a visit to get real answers. The Internet doesn’t know your medical history, and I wouldn’t try typing all of that into the Google search bar -- I've tried. Your body is different from the person in the article, despite how similar the author makes you out to appear.</p>
          <p>Tons of publications out there compete for our attention twenty-four hours a day, and they've developed a sneaky strategy that exploits our weaknesses (e.g. fear) to grab it. There's a time and a place for serious health concern, but chances are money-grubbing magazines, websites and television shows aren't the answer.</p>
